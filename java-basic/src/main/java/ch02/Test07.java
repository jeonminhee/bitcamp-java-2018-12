// -A(0x41), B(0x42), a(0x61), b(0x62), 스페이스(0x20), LF(0x0a), CR(0x0d)
// !(0x21), #(0x23), $(0x24), %(0x25), &(0x26), +(0x2b)
// 
// 
// -참고
// 윈도우 OS에서는 LFCR 두 바이트를 사용하여 줄 바꿈을 표시한다.
