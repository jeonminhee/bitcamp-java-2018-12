// 
// # 문자를 2진수로 변환하는 규칙 => 문자집합 (character set)
// 1)ASCII(American Standard Code for Information Interchange / 7bit)
// - 미국 국가 표준 협회(ANSI)에서 영어 알파벳, 숫자, 특수 문자 등을 컴퓨터에서 저장 할 수 있도록
